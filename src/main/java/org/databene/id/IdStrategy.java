package org.databene.id;

import java.util.HashMap;
import java.util.Map;

/**
 * Strategy for ID creation.<br/>
 * <br/>
 * Created: 30.01.2008 18:24:56
 * @since 0.4.0
 * @author Volker Bergmann
 */

public class IdStrategy<E> {
    
    private static Map<String, IdStrategy> instances = new HashMap<String, IdStrategy>();
    
    private String name;
    private Class<E> type;
    
    public IdStrategy(String name, Class<E> type) {
        super();
        this.name = name;
        this.type = type;
        instances.put(name, this);
    }

    public String getName() {
        return name;
    }
    
    public Class<E> getType() {
        return type;
    }
    
    @Override
    public String toString() {
        return name;
    }

    /**
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

    /**
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        final IdStrategy other = (IdStrategy) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        return true;
    }

    public static <T> IdStrategy<T> getInstance(String name) {
        return instances.get(name);
    }
    
}
