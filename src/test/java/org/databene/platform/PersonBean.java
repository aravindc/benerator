package org.databene.platform;

/**
 * Simple JavaBean for testing, it holds a 'name' and an 'age' property.<br/>
 * <br/>
 * Created: 04.08.2007 08:40:20
 * @author Volker Bergmann
 */
public class PersonBean {

    private String name;
    private int age;

    public PersonBean() {
        this("Unknown", -1);
    }

    public PersonBean(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return name + '(' + age + ')';
    }

    @Override
    public boolean equals(Object o) {
        if (this == o)
            return true;
        if (o == null || getClass() != o.getClass())
            return false;
        final PersonBean that = (PersonBean) o;
        return (this.age == that.age && this.name.equals(that.name));
    }

    @Override
    public int hashCode() {
        return name.hashCode() * 29 + age;
    }
    
}
